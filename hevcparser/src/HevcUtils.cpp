uint32_t HEVC::log2(uint32_t k)
{
  uint32_t res = 0;

  if (k & 0xffff0000) {
      k >>= 16;
      res += 16;
  }

  
  if (k & 0xff00) {
      k >>= 8;
      res += 8;
  }

  res += log2_tab[k];

  return res;
}
